// Normalize function for matching
function normalize(name) {
  return name.toLowerCase()
    .replace(/[^a-z0-9]/g, '')
    .replace(/^(the|universityof|univof|univ|collegeof|college)/, '')
    .trim();
}

// Match function
function findMatch(name1, list2) {
  const norm1 = normalize(name1);
  
  // Exact match first
  for (const name2 of list2) {
    if (normalize(name2) === norm1) {
      return name2;
    }
  }
  
  // Contains match (e.g., "UCF" matches "UCF Knights")
  for (const name2 of list2) {
    const norm2 = normalize(name2);
    if (norm1.includes(norm2) || norm2.includes(norm1)) {
      return name2;
    }
  }
  
  // Partial word match
  const words1 = norm1.split(/(?=[A-Z])|[\s-]+/).filter(w => w.length > 2);
  for (const name2 of list2) {
    const norm2 = normalize(name2);
    const words2 = norm2.split(/(?=[A-Z])|[\s-]+/).filter(w => w.length > 2);
    if (words1.some(w => words2.includes(w)) && words2.some(w => words1.includes(w))) {
      return name2;
    }
  }
  
  return null;
}

// Generate matches
